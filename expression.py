'''
    Operator	Name	        Example
        -	Subtraction	        x - y
        *	Multiplication	    x * y
        /	Division	        x / y
        %	Modulus	            x % y
        **	Exponentiation	    x ** y
        //	Floor division	    x // y
'''

x = 2 #Multiplication
x = x + 2
print(x)

x = 2   #Division
x = x / 2
print(x)

x = 2   #Multiplication
x = x * 2
print(x, ' = Multiplication')

x = 2   #Exponentiation / power
x = x ** 2
print(x)

x = 2   #floor division
x = x // 2
print(x)

